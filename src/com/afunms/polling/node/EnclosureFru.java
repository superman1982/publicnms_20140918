package com.afunms.polling.node;

public class EnclosureFru {
	private String fru;
	private String hwComponent;
	private String identification;
	private String idStatus;
	public String getFru() {
		return fru;
	}
	public void setFru(String fru) {
		this.fru = fru;
	}
	public String getHwComponent() {
		return hwComponent;
	}
	public void setHwComponent(String hwComponent) {
		this.hwComponent = hwComponent;
	}
	public String getIdentification() {
		return identification;
	}
	public void setIdentification(String identification) {
		this.identification = identification;
	}
	public String getIdStatus() {
		return idStatus;
	}
	public void setIdStatus(String idStatus) {
		this.idStatus = idStatus;
	}
	
}
