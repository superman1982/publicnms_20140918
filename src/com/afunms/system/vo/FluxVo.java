package com.afunms.system.vo;

public class FluxVo {
	
	private String date;
	
	private String fluxin;
	
	private String fluxout;
	
	public String getDate() {
		return date;
	}
	
	public void setDate(String date) {
		this.date = date;
	}
	
	public String getFluxin() {
		return fluxin;
	}
	
	public void setFluxin(String fluxin) {
		this.fluxin = fluxin;
	}
	
	public String getFluxout() {
		return fluxout;
	}
	
	public void setFluxout(String fluxout) {
		this.fluxout = fluxout;
	}
}