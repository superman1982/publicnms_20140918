package com.afunms.detail.reomte.model;

public class PagePerfInfo {
	
	/**
	 * 索引
	 */
	private String sindex;
	
	/**
	 * 页面调度程序输入/输出列表
	 */
	private String re;
	
	/**
	 * 内存页面调进数
	 */
	private String pi;
	
	/**
	 * 内存页面调出数
	 */
	private String po;
	
	/**
	 * 释放的页数
	 */
	private String fr;
	
	/**
	 * 扫描的页
	 */
	private String sr;
	
	/**
	 * 时钟周期
	 */
	private String cy;

	/**
	 * @return the sindex
	 */
	public String getSindex() {
		return sindex;
	}

	/**
	 * @param sindex the sindex to set
	 */
	public void setSindex(String sindex) {
		this.sindex = sindex;
	}

	/**
	 * @return the re
	 */
	public String getRe() {
		return re;
	}

	/**
	 * @param re the re to set
	 */
	public void setRe(String re) {
		this.re = re;
	}

	/**
	 * @return the pi
	 */
	public String getPi() {
		return pi;
	}

	/**
	 * @param pi the pi to set
	 */
	public void setPi(String pi) {
		this.pi = pi;
	}

	/**
	 * @return the po
	 */
	public String getPo() {
		return po;
	}

	/**
	 * @param po the po to set
	 */
	public void setPo(String po) {
		this.po = po;
	}

	/**
	 * @return the fr
	 */
	public String getFr() {
		return fr;
	}

	/**
	 * @param fr the fr to set
	 */
	public void setFr(String fr) {
		this.fr = fr;
	}

	/**
	 * @return the sr
	 */
	public String getSr() {
		return sr;
	}

	/**
	 * @param sr the sr to set
	 */
	public void setSr(String sr) {
		this.sr = sr;
	}

	/**
	 * @return the cy
	 */
	public String getCy() {
		return cy;
	}

	/**
	 * @param cy the cy to set
	 */
	public void setCy(String cy) {
		this.cy = cy;
	}

	
	
}
